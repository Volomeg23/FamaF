char *string_clone(const char *str, size_t length) {
    char *output=NULL;
    output = malloc(length*sizeof(char));  // *output no estaba inicializado por lo tanto cuando se intenta copiar la variable output no sobrevive
    //fuera de esta funcion por lo tanto se le asigna memoria dinamica para poder modificarlo mas tarde agregando lo que falta y que copy 
    // pueda contener la copia exacta de original 
    for (size_t i=0; i<length; i++) {
        output[i] = str[i];
    }
    output[length] = '\0';
    return output;
}


